package calculator;

public class Operation {
    public static int plus(int preNum, int postNum) {
        return preNum + postNum;
    }

    public static int minus(int preNum, int postNum) {
        return preNum - postNum;
    }

    public static int multiple(int preNum, int postNum) {
        return preNum * postNum;
    }

    public static int division(int preNum, int postNum) {
        return preNum / postNum;
    }
}
